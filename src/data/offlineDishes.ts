function createChefDish(seed: ChefDishSeed, index: number): DinnerDish {
  const fatG = Math.max(8, Math.round((seed.calories - seed.proteinG * 4 - 120) / 18));
  const carbsG = Math.max(8, Math.round((seed.calories - seed.proteinG * 4 - fatG * 9) / 4));
  const reasons = [
    seed.tags.includes("汤") ? "适合想吃温暖舒服的一餐" : "适合想快速做出有记忆点的家常味道",
    seed.tags.includes("高蛋白") || seed.proteinG >= 34 ? "蛋白质充足，晚餐饱腹感稳定" : "口味明确，不需要复杂准备",
    seed.tags.includes("下饭") || seed.carb ? "很适合作为今晚的主菜或盖饭" : "负担轻，适合搭配其他主菜",
  ];

  return {
    id: `chef-${seed.id}`,
    name: seed.name,
    description: seed.description,
    recommendationReason: reasons,
    weatherContext,
    recommendationScore: 84 + (index % 12),
    nutrition: {
      calories: seed.calories,
      proteinG: seed.proteinG,
      fatG,
      carbsG,
    },
    cooking: {
      timeMinutes: seed.timeMinutes,
      difficulty: seed.difficulty ?? (seed.timeMinutes > 60 ? "medium" : "easy"),
    },
    tags: seed.tags,
    ingredients: [
      { name: seed.protein, amount: seed.protein.includes("鸡蛋") ? "2 个" : "300g", category: "protein" },
      ...seed.vegetables.map((name) => ({ name, amount: "适量", category: "vegetable" as const })),
      ...(seed.carb ? [{ name: seed.carb, amount: "1-2 份", category: "carb" as const }] : []),
      ...seed.seasonings.map((name) => ({ name, amount: "适量", category: "seasoning" as const })),
    ],
    instructions: [
      "主食材提前处理干净，肉类切成适合入口的大小并简单调味。",
      seed.tags.includes("汤") ? "先处理汤底或焯水去浮沫，再加入耐煮食材小火炖出鲜味。" : "热锅下油，先把香味来源炒出来，再放主食材快速定型。",
      "加入蔬菜和调味，按口味调整咸淡，保持该脆的脆、该嫩的嫩。",
      seed.tags.includes("下饭") || seed.carb ? "收汁或调到适合拌饭的浓度后出锅。" : "出锅前补一点胡椒或香油提香。",
    ],
  };
}

